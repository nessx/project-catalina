package com.team.projectcatalina.clases;

public class Estacion {
    private String Name;
    private String Estado;

    public Estacion() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getEstado() {
        return Estado;
    }

    public void setEstado(String estado) {
        Estado = estado;
    }
}
